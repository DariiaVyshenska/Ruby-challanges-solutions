class CircularQueue
  def initialize(len)
    @len = len
    @queue = Array.new
  end

  def dequeue
    @queue.shift
  end

  def enqueue(el)
    dequeue if full?
    @queue.push(el)
  end

  private

  attr_reader :len, :queue

  def full?
    queue.size == len
  end
end

